export function getRank(input) {
    return RANK.find(
        rank => rank.rank === input 
    );
}

export function getType(input) {
    return TYPES.find(
        type => type.type === input 
    );
} 

export function getElement(input) {
    return ELEMENTS.find(
        type => type.name === input 
    );
} 

export function getFaction(input) {
    return FACTIONS.find(
        faction => faction.name === input 
    );
} 

export function getGift(input) {
    return GIFTS.find(
        gift => gift.type === input 
    );
} 
